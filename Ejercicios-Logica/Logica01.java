public class Logica01 {

    /*1) Declara dos variables numéricas (con el valor que desees), 
    muestra por consola la suma, resta, multiplicación, división y 
    módulo (resto de la división).*/
    public static void main(String[] args) throws Exception {
        
        //Declaramos las dos variables que operamos
        int num1 = 10;
        int num2 = 5;

        //Mostramos los Resultados de las operaciones
        System.out.println("Resultado de la Suma: " + (10+5));

        System.out.println("Resultado de la Resta: " + (10-5));

        System.out.println("Resultado de la Multiplicacion: " + (10*5));

        System.out.println("Resultado de la Division: " + (10/5));

        System.out.println("Resultado de la Division (Modulo): " + (10%5));
    }
}
